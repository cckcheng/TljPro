    private void addLongText(Container p, String txt) {
        while (txt.length() > 0) {
            int idx = txt.indexOf(" ");
            if (idx < 0) {
                p.add(new Label(txt));
                return;
            }
            p.add(new Label(txt.substring(0, idx)));
            txt = txt.substring(idx + 1);
        }
    }

